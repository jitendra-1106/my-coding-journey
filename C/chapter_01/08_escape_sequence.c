#include <stdio.h>
int main(){
    printf("Hey I am good \n nice");// \n=New line (next line me le jaata hai)
    printf("HeyI am good \t nice");// \t=Tab (space deta hai)
    printf("Hey I am good \" nice");// \"=Double quote print karne ke liye
    printf("Hey I am good \' nice");// \'= Single quote print karne ke liye
    printf("Hey I am good \\ nice");// \\=Backslash print karne ke liye
    printf("Hey I am good \\n nice");// \\n=Backslashn print karne ke liye
    return 0;
}
/*
others-
\b=Backspace (ek character piche hataata hai)
\r=Carriage return (line ke start me le jaata hai)
\a=Alert (beep sound)
\0=Null character (string ka end)*/